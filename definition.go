//Customer_SetVendorList对外提供重载vendor的方法
//已存在的不允许重载
//vendortype不为TYPE4的均设置为IETF
func Customer_SetVendorList(vendorid int, name string, typ string) error {
	id := VendorId(vendorid)
	if id == VENDOR_NO {
		return ERR_SET_VENDOR //不允许重载vendorid为0的
	}
	if _, ok := list_vendor_id[id]; ok { //若存在，则不允许重载
		return ERR_SET_VENDOR
	}
	name = stringfix(name)
	typ = stringfix(typ)
	if typ != "TYPE4" {
		typ = "IETF"
	}
	var c Description_definition = Description_definition{name, typ}
	list_vendor_id[id] = c
	list_vendor_name[name] = id
	return nil
}

//ListVender返回已加载的vendorid列表
func ListVender() map[VendorId]Description_definition {
	return list_vendor_id
}

//Customer_SetVendorAttList提供设置厂商属性列表的方法
//对于已经存在的属性，返回设置错误
//需要vendorname可识别，对于新厂商，需要先使用Customer_SetVendorList方法注册厂商
//其属性值类型必须符合规范，必须是INTEGER 、STRING 、IPADDR 、TAG_INT 、TAG_STR 、HEXADECIMAL之一
func Customer_SetVendorAttList(vendorname string, vaid_in int, vaname string, vatype string) error {
	vid, err := GetVendorId(vendorname)
	if err != nil {
		return err
	}
	vatype = stringfix(vatype)
	if !IsValidAttributeValueType(vatype) {
		return ERR_ATTV_TYPE
	}
	vaname = stringfix(vaname)
	var c Description_definition = Description_definition{vaname, vatype}

	_, ok := list_attV_id[vid][vaid_in]
	if ok {
		return ERR_ATT_SET
	} else {
		list_attV_id[vid][vaid_in] = c
		list_attV_name[vid][vaname] = vaid_in
	}
	return nil
}

//ListattV返回所有厂商属性列表
func ListattV() map[VendorId]map[int]Description_definition {
	return list_attV_id
}
